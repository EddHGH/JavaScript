//Também chamado de interação ou LOOP
//O for é um LOOP definido ou seja usado mais quando se sabe quantas iterações ocorrerão.
//Estrutura: For(inicializador;condição; incremento/decremento)
console.clear()
for(let i = 100;i>=0;i--){// Começa com 0, repete enquanto i for < 10 e soma +1 a I até i = 10.
    console.log(i)
//Contador de 100 a 0
}

